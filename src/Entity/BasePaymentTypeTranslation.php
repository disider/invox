namespace App\Entity;

use Symfony\Component\Validator\Constraints as Assert;

class BasePaymentTypeTranslation
{
    /**
     * @Assert\NotBlank(message="error.empty_name")
     * @var string
     */
    protected $name;

    public function setName($name)
    {
        $this->name = $name;
    }

    public function getName()
    {
        return $this->name;
    }
}
